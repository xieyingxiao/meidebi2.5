package com.meidebi.app.service.bean.detail;

public class InventoryBean {
	private String id;
	private String showdanid;
	private String url;
	private String title;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getShowdanid() {
		return showdanid;
	}
	public void setShowdanid(String showdanid) {
		this.showdanid = showdanid;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
}
